<template>
  <li>
    <h3>{{ fullName }}</h3>
    <h4>{{ rate }}/hour</h4>
    <div>
      <base-badge
        v-for="area in areas"
        :key="area"
        :mode="area"
        :title="area"
      ></base-badge>
    </div>
    <div class="actions">
      <base-button isLink :to="coachContactLink" mode="outline"
        >Contact</base-button
      >
      <base-button isLink :to="coachDetailLink">View Details</base-button>
    </div>
  </li>
</template>

<script>
export default {
  name: 'CoachItem',
  props: ['id', 'firstName', 'lastName', 'rate', 'areas'],
  computed: {
    fullName() {
      return `${this.firstName} ${this.lastName}`;
    },
    coachContactLink() {
      return {
        path: `/coaches/${this.id}/contact`
      };
    },
    coachDetailLink() {
      return {
        path: `/coaches/${this.id}`
      };
    }
  }
};
</script>

