<template>
  <base-card>
    <h2>Find Your Coach</h2>
    <span class="filter-option">
      <input
        type="checkbox"
        id="frontend"
        name="area"
        v-model="selectedAreaValue"
        value="frontend"
        @change="selectedAreaChange"
      />
      <label for="frontend">Frontend</label>
    </span>
    <span class="filter-option">
      <input
        type="checkbox"
        id="backend"
        name="area"
        v-model="selectedAreaValue"
        value="backend"
        @change="selectedAreaChange"
      />
      <label for="backend">Backend</label>
    </span>
    <span class="filter-option">
      <input
        type="checkbox"
        id="career"
        name="area"
        v-model="selectedAreaValue"
        value="career"
        @change="selectedAreaChange"
      />
      <label for="career">Career</label>
    </span>
  </base-card>
</template>

<script>
export default {
  name: 'CoachFilter',
  emits: ['area-change'],
  data() {
    return {
      selectedAreaValue: ['frontend', 'backend', 'career']
    };
  },
  methods: {
    selectedAreaChange() {
      this.$emit('area-change', this.selectedAreaValue);
    }
  }
};
</script>

